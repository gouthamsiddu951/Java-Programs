public class LogicalOperatorsBasic {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        System.out.println("x AND y (x && y): " + (x && y));

        System.out.println("x OR y (x || y): " + (x || y));

        System.out.println("NOT x (!x): " + !x);
    }
}
